/** Configure pins as 
              * Analog 
              * Input 
              * Output
              * EVENT_OUT
              * EXTI
*/
void ATC_GPIO_Init(void)
{
    GPIO_SetFunc(LED_1_Pin, GPIO_FUN0);
    GPIO_SetFunc(JDQ_1_Pin, GPIO_FUN0);
    GPIO_SetDir(LED_1_Pin, GPIO_OUT);
    GPIO_SetDir(JDQ_1_Pin, GPIO_OUT);
    GPIO_SetPinValue(LED_1_Pin, GPIO_LEVEL_HIGH);
    GPIO_SetPinValue(JDQ_1_Pin, GPIO_LEVEL_LOW);
    GPIO_SetE4E2(LED_1_Pin, GPIO_DRIVING_16MA);
    GPIO_SetE4E2(JDQ_1_Pin, GPIO_DRIVING_16MA);
}

