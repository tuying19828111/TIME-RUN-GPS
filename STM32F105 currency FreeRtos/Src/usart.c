//串口中断回调函数
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	/* 判断是哪个串口触发的中断 */
    if(huart->Instance == USART1)
    {
		RxLine++;                      //每接收到一个数据，进入回调数据长度加1
    DataBuff[RxLine-1]=RxBuff;     //把每次接收到的数据保存到缓存数组
		Rx_flag=1; 
    RxBuff=0;
    HAL_UART_Receive_IT(&huart1, &RxBuff, 1); //每接收一个数据，就打开一次串口中断接收，否则只会接收一个数据就停止接收
//		__HAL_TIM_SET_COUNTER(&htim3, 1); // 计数清零，从头开始计
    }
		if(huart->Instance == USART2)
    {
		RxLine_1++;                      //每接收到一个数据，进入回调数据长度加1
    DataBuff_1[RxLine_1-1]=RxBuff_1;     //把每次接收到的数据保存到缓存数组
		Rx_flag_1=1; 
    RxBuff_1=0;
    HAL_UART_Receive_IT(&huart2, &RxBuff_1, 1); //每接收一个数据，就打开一次串口中断接收，否则只会接收一个数据就停止接收
//		__HAL_TIM_SET_COUNTER(&htim3, 1); // 计数清零，从头开始计
    }
}


/**
  * 函数功能: 重定向c库函数printf到DEBUG_USARTx
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明：无
  */
int fputc(int ch, FILE *f)
{
  HAL_UART_Transmit(&huart1, (uint8_t *)&ch, 1, 0xffff);
  return ch;
}
 
/**
  * 函数功能: 重定向c库函数getchar,scanf到DEBUG_USARTx
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明：无
  */
int fgetc(FILE *f)
{
  uint8_t ch = 0;
  HAL_UART_Receive(&huart1, &ch, 1, 0xffff);
  return ch;
}

